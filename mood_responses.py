# mood_responses.py
def mood_response(mood):
    if mood == "happy":
        return "Glad to hear that you're feeling happy!"
    elif mood == "sad":
        return "I'm sorry to hear that you're feeling sad. Is there anything I can do to help?"
    elif mood == "excited":
        return "Great to see you're feeling excited! Keep up the enthusiasm!"
    else:
        return "I'm not sure how to respond to that mood. Please try again with a different mood."

